iris <- read.csv("iris.csv")
qqnorm(iris$Sepal.Length, main = "Normal Q-Q Plot for Sepal Length")
qqline(iris$Sepal.Length)
qqnorm(iris$Sepal.Width, main = "Normal Q-Q Plot for Sepal Width")
qqline(iris$Sepal.Width)
qqnorm(iris$Petal.Length, main = "Normal Q-Q Plot for Petal Length")
qqline(iris$Petal.Length)
qqnorm(iris$Petal.Width, main = "Normal Q-Q Plot for Petal Width")
qqline(iris$Petal.Width) 
hist(iris$Sepal.Width, main = "Histogram of Sepal Width", xlab="Sepal Width")
boxplot(iris$Sepal.Width ~ iris$Species,main = "Boxplot for Homogeneity of Variance")
lm.iris<-lm(Sepal.Width~Species,data=iris)
anova(lm.iris)
summary(lm.iris)
iris.aov<-aov(Sepal.Width~Species,data=iris)
TukeyHSD(iris.aov)
